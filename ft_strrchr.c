char	*ft_strrchr(const char *src, int c)
{
	int		i;
	char	*p;

	i = 0;
	p = 0;
	while (src[i])
	{
		if (src[i] == c)
		{
			p = &src[i];
		}
		i++;
	}
	if (c == '\0')
	{
		return ((char *)&src[i]);
	}
	return ((char *)p);
}
