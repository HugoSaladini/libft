char	*ft_strchr(const char *src, int c)
{
	int	i;

	i = 0;
	while(src[i])
	{
		if (src[i] == c)
		{
			return ((char *)&src[i]);
		}
		i++;
	}
	if (c == '\0')
	{
		return ((char *)&src[i]);
	}
	return (0);
}
